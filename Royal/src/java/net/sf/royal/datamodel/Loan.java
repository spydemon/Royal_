package net.sf.royal.datamodel;

import java.util.Date;

public class Loan extends ModelImpl {

    private int maxDays;
    private Date begin;
    private Date end;
    private float priceByDay;
    private float penaltyByDay;
    
    private Album album;
    private Borrower borrower;
    
    /**
     * @return Returns the begin.
     */
    public Date getBegin() {
        return begin;
    }
    /**
     * @param begin The begin to set.
     */
    public void setBegin(Date begin) {
        this.begin = begin;
    }
    /**
     * @return Returns the end.
     */
    public Date getEnd() {
        return end;
    }
    /**
     * @param end The end to set.
     */
    public void setEnd(Date end) {
        this.end = end;
    }
    /**
     * @return Returns the maxDays.
     */
    public int getMaxDays() {
        return maxDays;
    }
    /**
     * @param maxDays The maxDays to set.
     */
    public void setMaxDays(int maxDays) {
        this.maxDays = maxDays;
    }
    /**
     * @return Returns the album.
     */
    public Album getAlbum() {
        return album;
    }
    /**
     * @param album The album to set.
     */
    public void setAlbum(Album album) {
        this.album = album;
    }
    /**
     * @return Returns the penaltyByDay.
     */
    public float getPenaltyByDay() {
        return penaltyByDay;
    }
    /**
     * @param penaltyByDay The penaltyByDay to set.
     */
    public void setPenaltyByDay(float penaltyByDay) {
        this.penaltyByDay = penaltyByDay;
    }
    /**
     * @return Returns the priceByDay.
     */
    public float getPriceByDay() {
        return priceByDay;
    }
    /**
     * @param priceByDay The priceByDay to set.
     */
    public void setPriceByDay(float priceByDay) {
        this.priceByDay = priceByDay;
    }
    /**
     * @return Returns the borrower.
     */
    public Borrower getBorrower() {
        return borrower;
    }
    /**
     * @param borrower The borrower to set.
     */
    public void setBorrower(Borrower borrower) {
        this.borrower = borrower;
    }
    
    
}
