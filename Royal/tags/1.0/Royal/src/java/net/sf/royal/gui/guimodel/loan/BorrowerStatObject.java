package net.sf.royal.gui.guimodel.loan;

import java.util.List;

import net.sf.royal.datamodel.Album;

public class BorrowerStatObject {
    
    private List<Album> distribList;
    private int nbLoans;
    private float totalFine;
    private float totalPenalty;
    private int dayInLate;
    private float averageLoan;

    public List<Album> getDistribList() {
        return distribList;
    }

    public void setDistribList(List<Album> distribList) {
        this.distribList = distribList;
    }

    public float getAverageLoan() {
        return averageLoan;
    }

    public void setAverageLoan(float averageLoan) {
        this.averageLoan = averageLoan;
    }

    public int getDayInLate() {
        return dayInLate;
    }

    public void setDayInLate(int dayInLate) {
        this.dayInLate = dayInLate;
    }

    public int getNbLoans() {
        return nbLoans;
    }

    public void setNbLoans(int nbLoans) {
        this.nbLoans = nbLoans;
    }

    public float getTotalFine() {
        return totalFine;
    }

    public void setTotalFine(float totalFine) {
        this.totalFine = totalFine;
    }

    public float getTotalPenalty() {
        return totalPenalty;
    }

    public void setTotalPenalty(float totalPenalty) {
        this.totalPenalty = totalPenalty;
    }

}
